#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	int		i;
	char	*t;

	t = (char *)s;
	i = ft_strlen(t);
	while (i >= 0)
	{
		if (t[i] == (char)c)
			return (&t[i]);
		i --;
	}
	return (NULL);
}
